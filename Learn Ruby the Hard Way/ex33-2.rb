i = 0
numbers = []

while i < 6 # as long as i is less than the number 6
    puts "At the top i is #{i}"
    numbers.push(i) # adds i to end of numbers array

    i += 1 # adds 1 to the i variable
    puts "Numbers now: ", numbers
    puts "At the bottom i is #{i}"
end

puts "The numbers: "

